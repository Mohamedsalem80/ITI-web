let fruit = "apple";

switch (fruit.toLowerCase()) {
    case "apple":
        console.log("You chose an Apple");
        break;
    case "banana":
        console.log("You chose a Banana");
        break;
    case "orange":
        console.log("You chose an Orange");
        break;
    case "mango":
        console.log("You chose a Mango");
        break;
    default:
        console.log("Unknown fruit");
}
